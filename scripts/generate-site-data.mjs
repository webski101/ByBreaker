// scripts/generate-site-data.mjs
//
// Bakes the real demo artifacts (examples/sample-run/*) and the real
// compiled policy (via the real compiler.js — not retyped) into four tiny
// Vercel serverless functions under site/api/. Each one just returns a
// fixed JSON payload — no filesystem reads at request time, so there's
// zero risk of Vercel's bundler failing to trace a data file.
//
// This deliberately serves ONE static, reproducible demo run. It is not a
// live re-execution of the engine — see site/README.md for why, and how
// to host the genuinely live mode elsewhere.
//
// Run with: node scripts/generate-site-data.mjs

import { readFileSync, writeFileSync, copyFileSync, mkdirSync, cpSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { compilePolicy } from '../src/compiler.js';

const root = dirname(dirname(fileURLToPath(import.meta.url)));

const auditLog = readFileSync(join(root, 'examples/sample-run/output-audit-log.jsonl'), 'utf8')
  .trim()
  .split('\n')
  .filter(Boolean)
  .map((l) => JSON.parse(l));

const trades = readFileSync(join(root, 'examples/sample-run/input-trades.jsonl'), 'utf8')
  .trim()
  .split('\n')
  .filter(Boolean)
  .map((l) => JSON.parse(l));

const metrics = JSON.parse(readFileSync(join(root, 'examples/sample-run/output-metrics.json'), 'utf8'));
const summary = JSON.parse(readFileSync(join(root, 'examples/sample-run/output-summary.json'), 'utf8'));
const policyText = readFileSync(join(root, 'policies/example.policy.txt'), 'utf8');

const rules = compilePolicy(policyText).map((r) => ({
  id: r.id,
  action: r.action,
  metric: r.metric,
  description: r.describe(),
  source: r.source,
}));

function writeJsonHandler(name, data) {
  const code = `// Auto-generated by scripts/generate-site-data.mjs — do not hand-edit.
// Serves a fixed snapshot of the real, reproducible demo run committed at
// examples/sample-run/. Re-run the generator after a fresh \`npm run demo\`
// to update this.

const DATA = ${JSON.stringify(data, null, 2)};

export default function handler(req, res) {
  res.status(200).json(DATA);
}
`;
  writeFileSync(join(root, 'site/api', `${name}.js`), code);
}

function writeTextHandler(name, text) {
  const code = `// Auto-generated by scripts/generate-site-data.mjs — do not hand-edit.

const TEXT = ${JSON.stringify(text)};

export default function handler(req, res) {
  res.setHeader('content-type', 'text/plain');
  res.status(200).send(TEXT);
}
`;
  writeFileSync(join(root, 'site/api', `${name}.js`), code);
}

mkdirSync(join(root, 'site/api'), { recursive: true });
writeJsonHandler('log', auditLog);
writeJsonHandler('metrics', metrics);
writeJsonHandler('summary', summary);
writeJsonHandler('rules', rules);
writeJsonHandler('trades', trades);
writeTextHandler('policy', policyText);

mkdirSync(join(root, 'site'), { recursive: true });
copyFileSync(join(root, 'dashboard/index.html'), join(root, 'site/index.html'));
cpSync(join(root, 'dashboard/lib'), join(root, 'site/lib'), { recursive: true });

console.log('✓ Generated site/api/{log,metrics,summary,rules,trades,policy}.js, site/index.html, site/lib/');
